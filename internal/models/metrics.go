package models

// Counter представляет тип метрики "counter" - счётчик, который только увеличивается
const Counter = "counter"

// Gauge представляет тип метрики "gauge" - датчик, который может принимать любые числовые значения
const Gauge = "gauge"

// Metrics представляет структуру метрики для сериализации в JSON и передачи между компонентами системы.
//
// Структура использует плоскую модель без иерархической вложенности для упрощения.
// Поля Delta и Value объявлены через указатели, чтобы отличать значение "0" от не заданного значения
// и соответственно не кодировать нулевые значения в JSON.
//
// Поля:
//   - ID: уникальный идентификатор метрики
//   - MType: тип метрики ("counter" или "gauge")
//   - Delta: указатель на значение счётчика (только для типа "counter")
//   - Value: указатель на значение датчика (только для типа "gauge")
//   - Hash: хеш-значение для проверки целостности данных (опционально)
type Metrics struct {
	ID    string   `json:"id"`
	MType string   `json:"type"`
	Delta *int64   `json:"delta,omitempty"`
	Value *float64 `json:"value,omitempty"`
	Hash  string   `json:"hash,omitempty"`
}

package crypto

// DecryptorInterface определяет интерфейс для дешифрования данных.
//
// Этот интерфейс позволяет использовать различные реализации дешифрования
// (RSA, гибридное шифрование и т.д.) с одинаковым API.
type DecryptorInterface interface {
	// Decrypt дешифрует данные.
	//
	// Параметры:
	//   - data: зашифрованные данные
	//
	// Возвращает:
	//   - []byte: дешифрованные данные
	//   - error: ошибка при дешифровании
	Decrypt(data []byte) ([]byte, error)
}

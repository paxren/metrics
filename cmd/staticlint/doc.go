// Package main реализует статический анализатор кода staticlint.
//
// Staticlint - это инструмент для статического анализа Go кода, который использует
// фреймворк golang.org/x/tools/go/analysis/multichecker для запуска множества
// анализаторов. Анализатор включает стандартные анализаторы из пакета
// golang.org/x/tools/go/analysis/passes, пользовательские анализаторы и анализаторы
// из пакета staticcheck.
//
// Основные компоненты:
//   - ErrCheckAnalyzer: проверяет наличие необработанных ошибок
//   - osexit.Analyzer: проверяет прямые вызовы os.Exit в функции main
//   - Стандартные анализаторы: asmdecl, assign, atomic, bools и другие
//   - Анализаторы staticcheck: настраиваются через конфигурационный файл
//
// Конфигурация:
// Анализатор использует файл config.json для настройки анализаторов staticcheck.
// Файл должен находиться в той же директории, что и исполняемый файл.
//
// Использование:
//
//	staticlint <пакеты>
//
// Пример:
//
//	staticlint ./...
